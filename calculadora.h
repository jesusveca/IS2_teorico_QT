#ifndef CALCULADORA
#define CALCULADORA

#endif // CALCULADORA

template <class T>
T clear(){
    return 0;
}

template <class T>
T sumar(T a, T b){
    return a+b;
}

template <class T>
T restar(T a, T b){
    return a-b;
}

template <class T>
T multiplicar(T a, T b){
    return a*b;
}


template <class T>
T dividir(T a, T b){
    return a/b;
}
